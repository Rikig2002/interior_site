export const getMockProjectById = (idOrSlug) => {
  const target = String(idOrSlug || '').trim().toLowerCase()
  return (
    mockProjects.find((project) => {
      const id = String(project.id || '').toLowerCase()
      const slug = String(project.slug || '').toLowerCase()
      return id === target || slug === target
    }) || null
  )
}

export const filterMockProjects = ({ category = '', city = '', search = '' } = {}) => {
  const categoryNormalized = String(category).trim().toLowerCase()
  const cityNormalized = String(city).trim().toLowerCase()
  const searchNormalized = String(search).trim().toLowerCase()

  return mockProjects.filter((project) => {
    const matchesCategory = !categoryNormalized || String(project.category).toLowerCase() === categoryNormalized
    const matchesCity = !cityNormalized || String(project.city).toLowerCase() === cityNormalized

    const searchable = [project.title, project.city, project.category, ...(project.tags || [])]
      .join(' ')
      .toLowerCase()

    const matchesSearch = !searchNormalized || searchable.includes(searchNormalized)

    return matchesCategory && matchesCity && matchesSearch
  })
}
